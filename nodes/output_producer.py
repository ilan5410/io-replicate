"""
Stage 5: Output Producer

Generates all tables and figures specified in replication_spec.outputs.

Architecture: direct LLM call → execute → one fix pass (per script type).
Tables and figures are generated by separate scripts to reduce per-script complexity.
No tool-calling loop — the full context is passed upfront.

Max LLM calls: 4 (tables generate + optional fix, figures generate + optional fix).
"""
import logging
import os
import re
import subprocess
import sys
import tempfile
from pathlib import Path

import pandas as pd
import yaml
from rich.console import Console
from rich.panel import Panel

from agents.state import PipelineState
from agents.prompts.output_producer import (
    OUTPUT_PRODUCER_TABLES_PROMPT,
    OUTPUT_PRODUCER_FIGURES_PROMPT,
    OUTPUT_PRODUCER_FIX_PROMPT,
)

log = logging.getLogger("output_producer")
_console = Console()
MAX_FIX_ATTEMPTS = 2


def output_producer_node(state: PipelineState) -> dict:
    """Generate tables and figures via direct LLM calls (no tool loop)."""
    run_dir = Path(state["run_dir"])
    config = state.get("config", {})
    spec = state["replication_spec"]

    n_tables = len(spec["outputs"].get("tables", []))
    n_figures = len(spec["outputs"].get("figures", []))
    _console.print(Panel(
        f"[bold]Stage 5 — Output Producer[/bold]\n"
        f"Generating {n_tables} tables and {n_figures} figures",
        style="blue"
    ))

    outputs_dir = run_dir / "outputs"
    tables_dir = outputs_dir / "tables"
    figures_dir = outputs_dir / "figures"
    tables_dir.mkdir(parents=True, exist_ok=True)
    figures_dir.mkdir(parents=True, exist_ok=True)

    context = _build_context(run_dir, spec)
    llm_client = _make_client(config)

    # ── Tables ────────────────────────────────────────────────────────────────
    if n_tables:
        tables_spec = yaml.dump(
            {"tables": spec["outputs"]["tables"]}, default_flow_style=False
        )
        tables_msg = _user_message_tables(context, tables_spec, tables_dir)
        ok = _run_script_with_fix(
            llm_client, config, OUTPUT_PRODUCER_TABLES_PROMPT,
            tables_msg, run_dir, "generate_tables",
        )
        if not ok:
            raise RuntimeError(
                "Table generation failed after fix attempt — reviewer would see 0 outputs. "
                "Check generated_scripts/generate_tables*.py and its stderr for details."
            )

    # ── Figures ───────────────────────────────────────────────────────────────
    if n_figures:
        figures_spec = yaml.dump(
            {"figures": spec["outputs"]["figures"]}, default_flow_style=False
        )
        figures_msg = _user_message_figures(context, figures_spec, figures_dir)
        ok = _run_script_with_fix(
            llm_client, config, OUTPUT_PRODUCER_FIGURES_PROMPT,
            figures_msg, run_dir, "generate_figures",
        )
        if not ok:
            raise RuntimeError(
                "Figure generation failed after fix attempt — reviewer would see 0 outputs. "
                "Check generated_scripts/generate_figures*.py and its stderr for details."
            )

    # ── Collect output paths ───────────────────────────────────────────────────
    output_paths: dict[str, str] = {}
    for table in spec["outputs"].get("tables", []):
        tid = table["id"]
        for ext in ("csv", "xlsx"):
            p = tables_dir / f"{tid}.{ext}"
            if p.exists():
                output_paths[f"{tid}_{ext}" if ext != "csv" else tid] = str(p)

    for figure in spec["outputs"].get("figures", []):
        fid = figure["id"]
        for ext in ("png", "pdf"):
            p = figures_dir / f"{fid}.{ext}"
            if p.exists():
                output_paths[f"{fid}_{ext}" if ext != "png" else fid] = str(p)

    produced = len(output_paths)
    expected = n_tables * 2 + n_figures * 2
    _console.print(
        f"  [{'green' if produced >= expected else 'yellow'}]"
        f"{'✓' if produced >= expected else '⚠'}[/] "
        f"{produced}/{expected} output files produced"
    )
    log.info(f"Output paths: {list(output_paths.keys())}")
    return {"output_paths": output_paths, "current_stage": 5}


# ---------------------------------------------------------------------------
# Context builder
# ---------------------------------------------------------------------------

def _build_context(run_dir: Path, spec: dict) -> str:
    """Build the full data context string passed to both table and figure generators."""
    decomp_dir = run_dir / "data" / "decomposition"
    lines = ["## Available data files\n"]

    if decomp_dir.exists():
        for csv_path in sorted(decomp_dir.glob("*.csv")):
            try:
                df = pd.read_csv(csv_path, nrows=5)
                lines.append(f"### {csv_path}  (absolute path)")
                lines.append(f"columns: {list(df.columns)}")
                lines.append(df.to_string(index=False, max_cols=10))
                lines.append("")
            except Exception as e:
                lines.append(f"### {csv_path}  (could not read: {e})\n")

    # Also load data_guide alignment notes if available
    guide_path = run_dir / "data" / "raw" / "data_guide.yaml"
    if guide_path.exists():
        try:
            guide = yaml.safe_load(guide_path.read_text()) or {}
            notes = guide.get("alignment", {}).get("notes", [])
            if notes:
                lines.append("## Data guide alignment notes")
                for note in notes:
                    lines.append(f"- {note}")
                lines.append("")
        except Exception:
            pass

    geo = spec.get("geography", {})
    country_codes = [e["code"] for e in geo.get("analysis_entities", [])]
    lines.append(f"## Country codes in data\n{country_codes}\n")

    return "\n".join(lines)


