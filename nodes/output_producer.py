def _user_message_tables(context: str, tables_spec: str, tables_dir: Path) -> str:
    return f"""{context}

## Tables to generate
```yaml
{tables_spec}
```

## Output directory (absolute)
{tables_dir}

Save each table as `<id>.csv` and `<id>.xlsx` in the output directory above.
Use the EXACT absolute paths from "Available data files" when reading input CSVs.
Write the complete Python script now.
"""


def _user_message_figures(context: str, figures_spec: str, figures_dir: Path) -> str:
    return f"""{context}

## Figures to generate
```yaml
{figures_spec}
```

## Output directory (absolute)
{figures_dir}

Save each figure as `<id>.png` (dpi=150) and `<id>.pdf` in the output directory above.
Use the EXACT absolute paths from "Available data files" when reading input CSVs.
Write the complete Python script now.
"""


# ---------------------------------------------------------------------------
# LLM call + execute + fix
# ---------------------------------------------------------------------------

def _run_script_with_fix(
    client,
    config: dict,
    system_prompt: str,
    user_message: str,
    run_dir: Path,
    script_name: str,
) -> bool:
    """Generate a script, execute it. On failure, ask the LLM to fix once."""
    model = _resolve_model(config, "output_producer")
    max_tokens = config.get("llm", {}).get("output_producer_max_tokens", 8000)

    # Generate
    _console.print(f"  [dim]Generating {script_name}...[/dim]")
    script = _call_llm(client, model, max_tokens, system_prompt, user_message)
    script = _strip_fences(script)

    success, stderr = _execute(script, run_dir, script_name)
    if success:
        _console.print(f"  [green]✓[/green] {script_name}")
        return True

    # One fix attempt
    _console.print(f"  [yellow]⚠[/yellow] {script_name} failed — fixing...")
    log.warning(f"{script_name} error:\n{stderr[:500]}")

    fix_message = (
        f"Error:\n```\n{stderr[:1500]}\n```\n\n"
        f"Original script:\n```python\n{script}\n```"
    )
    fixed_script = _call_llm(
        client, model, max_tokens, OUTPUT_PRODUCER_FIX_PROMPT, fix_message
    )
    fixed_script = _strip_fences(fixed_script)

    success, stderr2 = _execute(fixed_script, run_dir, f"{script_name}_fixed")
    if success:
        _console.print(f"  [green]✓[/green] {script_name} (fixed)")
        return True

    _console.print(f"  [red]✗[/red] {script_name} still failing after fix")
    log.error(f"{script_name} fix failed:\n{stderr2[:500]}")
    return False


def _call_llm(client, model: str, max_tokens: int, system: str, user: str) -> str:
    """Single Anthropic API call (streaming for large outputs)."""
    params = dict(
        model=model,
        max_tokens=max_tokens,
        system=system,
        messages=[{"role": "user", "content": user}],
    )
    with client.messages.stream(**params) as stream:
        response = stream.get_final_message()
    usage = response.usage
    log.info(
        f"output_producer LLM: {usage.input_tokens} in + {usage.output_tokens} out tokens "
        f"(~${(usage.input_tokens * 3 + usage.output_tokens * 15) / 1_000_000:.4f})"
    )
    return response.content[0].text.strip()


# Modules that generated scripts should never import
_FORBIDDEN_IMPORTS = frozenset({"subprocess", "socket", "urllib", "requests", "shutil"})


def _check_script_safety(script: str) -> None:
    """Raise ValueError if the script imports a forbidden module."""
    for mod in _FORBIDDEN_IMPORTS:
        if re.search(rf"\bimport\s+{mod}\b", script) or re.search(rf"\bfrom\s+{mod}\b", script):
            raise ValueError(
                f"Generated script imports forbidden module '{mod}'. "
                "Regeneration required."
            )


def _execute(script: str, run_dir: Path, name: str) -> tuple[bool, str]:
    """Write script to generated_scripts/ and execute it."""
    try:
        _check_script_safety(script)
    except ValueError as e:
        log.error(f"Script safety check failed for {name}: {e}")
        return False, str(e)

    scripts_dir = run_dir / "generated_scripts"
    scripts_dir.mkdir(exist_ok=True)
    script_path = scripts_dir / f"{name}.py"
    script_path.write_text(script)

    result = subprocess.run(
        [sys.executable, str(script_path)],
        cwd=str(run_dir),
        capture_output=True,
        text=True,
        timeout=120,
    )
    if result.returncode == 0:
        return True, ""
    return False, (result.stderr or result.stdout)[-2000:]


def _strip_fences(text: str) -> str:
    text = re.sub(r"^```python\s*\n", "", text, flags=re.IGNORECASE)
    text = re.sub(r"^```\s*\n", "", text, flags=re.MULTILINE)
    text = re.sub(r"\n```\s*$", "", text)
    return text.strip()


# ---------------------------------------------------------------------------
# LLM client
# ---------------------------------------------------------------------------

def _make_client(config: dict):
    import anthropic
    providers = config.get("llm", {}).get("providers", {})
    key_env = providers.get("anthropic", {}).get("api_key_env", "ANTHROPIC_API_KEY")
    api_key = os.environ.get(key_env)
    if not api_key:
        raise EnvironmentError(f"{key_env} not set")
    return anthropic.Anthropic(api_key=api_key)


def _resolve_model(config: dict, agent: str) -> str:
    routing = config.get("llm", {}).get("routing", {})
    model_str = routing.get(agent, "claude-sonnet-4-6")
    return model_str.split("/", 1)[1] if "/" in model_str else model_str
